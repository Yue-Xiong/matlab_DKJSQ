% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
huankuan=get(handles.popupmenu1,'Value');%通过读取控件Value来获取贷款方式

r=str2double(get(handles.edit2,'string'))*(1+str2double(get(handles.edit5,'string'))/100)/1200;%获取并计算月利率

yihang=get(handles.edit4,'String');%获取贷款银行

H=str2double(get(handles.edit3,'string'))*12;%获取贷款期数

P=str2double(get(handles.edit1,'string'));

axes(handles.axes1);

switch huankuan

case 1 % 等额本金

for M=1:H

A(M)=P*10000/H+(1-(M-1)/H)*P*r*10000;%等额本金计算公式

end

plot([1:H],A)

% ylim([min(A)-500 max(A) 500]);

xlim([0 H]);

title(yihang);

ylabel('每月还款金额（元）');

xlabel('还款月份');

case 2 % 等额本息

for M=1:H

A(M)=P*10000*r*((1+r)^H/((1+r)^H-1));

end

plot([1:H],A)

xlim([0 H]);

title(yihang)

ylabel('每月还款金额（元）');

xlabel('还款月份');

end

L=(sum(A)-P*10000)/10000;%计算总利息

set(handles.text10,'String',num2str(L));%在文本框lixi中显示总利息
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)



